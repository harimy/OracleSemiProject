  AND OPS.SJ_CODE = SJ.SJ_CODE(+)     -- OP_SUBJECT, SUBJECT
  AND D.SG_CODE(+) = SG.SG_CODE       -- DROP, SUGANG
  AND OPS.B_CODE = B.B_CODE(+)        -- OP_SUBJECT, BOOK
