    ON OS.P_CODE = PR.P_CODE
        JOIN TBL_SUBJECT SU
        ON OS.SJ_CODE = SU.SJ_CODE
            JOIN TBL_BOOK BO
            ON OS.B_CODE = BO.B_CODE
                JOIN TBL_ROOM TR
                ON OC.R_CODE = TR.R_CODE;
